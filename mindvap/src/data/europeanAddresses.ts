export const getCountryData = (countryCode: string): EuropeanCountry | null => {
  return EUROPEAN_COUNTRIES[countryCode.toUpperCase()] || null;
};

export const getCitiesForCountry = (countryCode: string): EuropeanCity[] => {
  const country = getCountryData(countryCode);
  return country?.cities || [];
};

export const getPostalCodeInfo = (countryCode: string) => {
  const country = getCountryData(countryCode);
  return country ? {
    pattern: country.postalCodePattern,
    placeholder: country.postalCodePlaceholder
  } : {
    pattern: '[0-9]{5}',
    placeholder: '12345'
  };
};
